text = "Python programming is fun"

# 1. Print the first character
print("1. First character:", text[0])

# 2. Print the last character
print("2. Last character:", text[-1])

# 3. Print the first 6 characters
print("3. First 6 characters:", text[:6])

# 4. Print the length of the string
print("4. Length of string:", len(text))

# 5. Print the string in uppercase
print("5. Uppercase:", text.upper())